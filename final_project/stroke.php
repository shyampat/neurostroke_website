                <h1> Brain Stem Stroke</h1>
                <p>Brain stem strokes can have complex symptoms, and they can be difficult to diagnose. A person may have vertigo, dizziness and severe imbalance without the hallmark of most strokes — weakness on one side of the body. The symptoms of vertigo dizziness or imbalance usually occur together; dizziness alone is not a sign of stroke. A brain stem stroke can also cause double vision, slurred speech and decreased consciousness.</p>
                <p>Only a half-inch in diameter, the brain stem controls all basic activities of the central nervous system: consciousness, blood pressure and breathing. All motor control for the body flows through it. Brain stem strokes can impair any or all of these functions. More severe brain stem strokes can cause locked-in syndrome, a condition in which survivors can move only their eye. <p>
                <p>If a stroke in the brain stem results from a clot, the faster blood flow can be restored, the better the chances for recovery. Patients should receive treatment as soon as possible for the best recovery.</p>
