                <h1> Cryptogenic Stroke</h1>
                <p>In most cases, a stroke is caused by a blood clot that blocks the flow of blood to the brain. In some instances, despite testing, the cause of a stroke can’t be determined. This is called a cryptogenic stroke.</p>
                <p>Because approximately 1 in 4 stroke survivors will likely have another stroke event, finding the cause of the stroke will help your physician treat the cause of your stroke and lower the likelihood of another. Having a cryptogenic stroke may be frustrating and overwhelming, but with a proper diagnostic workup and collaboration with your healthcare team, you can take part in finding the cause of your stroke and help prevent another one from occurring.</p>
                <p> It’s estimated that about 1 in 3 ischemic strokes are cryptogenic. </p>
