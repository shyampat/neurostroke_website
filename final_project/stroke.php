                <h1> Hemorrhagic Stroke (Bleeds)</h1>
                <p>Occurs when a weakened blood vessel ruptures. The two types of weakened blood vessels that usually cause hemorrhagic stroke are aneurysms and arteriovenous malformations (AVMs). The most common cause of hemorrhagic stroke is uncontrolled high blood pressure.</p>
                <p>Hemorrhagic strokes make up about 13 % of stroke cases. They're caused by a weakened vessel that ruptures and bleeds into the surrounding brain. The blood accumulates and compresses the surrounding brain tissue.</p>
