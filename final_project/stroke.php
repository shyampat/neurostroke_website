                <h1> Ischemic Stroke (Clots)</h1>
                <p>Occurs when a blood vessel supplying blood to the brain is obstructed. It accounts for 87 % of all strokes.</p>
                <p>Fatty deposits lining the vessel walls, called atherosclerosis, are the main cause for ischemic stroke. Fatty deposits can cause two types of obstruction:</p>
                <ul>
                    <li>Cerebral thrombosis is a thrombus (blood clot) that develops at the fatty plaque within the blood vessel.</li>
                    <li>Cerebral embolism is a blood clot that forms at another location in the circulatory system, usually the heart and large arteries of the upper chest and neck. Part of the blood clot breaks loose, enters the bloodstream and travels through the brain’s blood vessels until it reaches vessels too small to let it pass. A main cause of embolism is an irregular heartbeat called atrial fibrillation. It can cause clots to form in the heart, dislodge and travel to the brain.</li>
                </ul>
            </div>
