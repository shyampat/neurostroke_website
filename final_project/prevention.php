                <h2>Stop Smoking</h2>
                <p>Cigarette smoking greatly increases your chances of having a stroke. If you don’t smoke, don’t start. If you do smoke, quitting will lower your risk for stroke. Your doctor can suggest ways to help you quit.</p>
                <img id = "img_smoking" src = "https://cdn.pixabay.com/photo/2015/12/28/20/46/smoking-1111975__480.jpg" alt = "prevention_smoking">
