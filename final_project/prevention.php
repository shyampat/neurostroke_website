                <p>Having overweight or obesity increases your risk for stroke. To determine whether your weight is in a healthy range, doctors often calculate your body mass index (BMI). If you know your weight and height, you can calculate your BMI at CDC’s Assessing Your Weight website. Doctors sometimes also use waist and hip measurements to measure excess body fat.</p>
