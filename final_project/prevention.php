                <h2>Control Your Blood Pressure</h2>
                <p>High blood pressure usually has no symptoms, so be sure to have it checked on a regular basis. You can check your blood pressure at home, at a doctor’s office, or at a pharmacy.</p>
                <br>
                <p>If you have high blood pressure, your doctor might prescribe medicine, suggest some changes in your lifestyle, or recommend that you choose foods with lower sodium (salt).</p>
