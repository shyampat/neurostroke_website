                <h2>Healthy Food and Drinks</h2>
                <p>Choosing healthy meal and snack options can help you prevent stroke. Be sure to eat plenty of fresh fruits and vegetables.</p>
                <br>
                <p>Eating foods low in saturated fats, trans fat, and cholesterol and high in fiber can help prevent high cholesterol. Limiting salt (sodium) in your diet can also lower your blood pressure. High cholesterol and high blood pressure increase your chances of having a stroke.</p>
