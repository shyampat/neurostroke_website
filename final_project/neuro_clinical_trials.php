        <h1 class = "trial_h1">What are clinical trials?</h1>
        <p class = "trial_p">A clinical trial is a complete investigation of a medicine or a medical device by the Food and Drug Administration to ensure it’s safe and effective before it’s released to the public. Some clinical trials could benefit stroke survivors. Clinical trials are based on a set of rules called protocols, which describe eligibility, test schedule, procedures and medications, and study length. Participants are seen regularly by the research staff to monitor their health and to determine the safety and effectiveness of the treatment. Participants may decline to be a part of the trial or withdraw from it at any time.</p>
        <h1 class = "trial_h1">Are clinical research studies safe?</h1>
        <p class = "trial_p">The government has strict guidelines and safeguards to protect people who participate in clinical trials. Every clinical trial in the U.S. must be approved and monitored by an Institutional Review Board to make sure the risks are as low as possible and are worth any potential benefits. Before participating in a trial, a person must sign an informed consent agreement, which provides detailed information about the study, medications and procedures. Experienced, trained physicians who have been designated principal investigators closely monitor study participants.</p>
